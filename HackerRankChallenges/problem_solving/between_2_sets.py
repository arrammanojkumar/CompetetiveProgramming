def getTotalX(a, b):
    # Write your code here
    max_a = max(a)
    min_b = min(b) + 1
    
    selected = 0
    for choice in range(max_a, min_b):
        if all(choice % a_el == 0 for a_el in a) and all(b_el % choice == 0 for b_el in b):
            selected += 1
    return selected

